#include <iostream>
using namespace std;

int main() {
    int n;
    
    cout << "Введите n (1 <= n <= 999, последняя цифра ≠ 0): ";
    cin >> n;
    
    if (n < 1 || n > 999) {
        cout << "Ошибка: n должно быть в диапазоне от 1 до 999" << endl;
        return 1;
    }
    
    if (n % 10 == 0) {
        cout << "Ошибка: последняя цифра n не должна быть равна 0" << endl;
        return 1;
    }
    
    // Разбираем число n на цифры
    int c = n / 100;        // первая цифра n (последняя цифра исходного числа)
    int b = (n / 10) % 10;  // вторая цифра n (вторая цифра исходного числа)
    int a = n % 10;         // третья цифра n (первая цифра исходного числа)
    
    // Согласно условию:
    // Исходное число x = 100*A + 10*B + C
    // Зачеркнули последнюю цифру C → получили 10*A + B
    // Переставили цифры → получили 10*B + A
    // Слева приписали C → получили 100*C + 10*B + A = n
    
    // Из уравнения: 100*C + 10*B + A = n
    // Но в нашем разборе n = 100*c + 10*b + a
    // Значит: C = c, B = b, A = a
    
    int A = a;  // первая цифра исходного числа
    int B = b;  // вторая цифра исходного числа
    int C = c;  // третья цифра исходного числа
    
    int x = 100 * A + 10 * B + C;
    
    // Проверяем, что x действительно трехзначное число
    if (x >= 100 && x <= 999) {
        cout << "Найдено число x = " << x << endl;
        
        // Проверка
        cout << "\nПроверка:" << endl;
        cout << "Исходное число: " << x << " (" << A << B << C << ")" << endl;
        cout << "Зачеркнули последнюю цифру " << C << ": получили " << (10*A + B) << endl;
        cout << "Переставили цифры: получили " << (10*B + A) << endl;
        cout << "Слева приписали " << C << ": получили " << (100*C + 10*B + A) << endl;
        cout << "Результат: " << (100*C + 10*B + A) << " (должно быть " << n << ")" << endl;
    } else {
        cout << "Для n = " << n << " трехзначное число x не найдено" << endl;
    }
    
    return 0;
}
