#include <iostream>

int main() {
    int n;
    std::cout << "Введите число от 0 до 9999: ";
    std::cin >> n;

    // Проверяем, что число является четырехзначным или имеет менее 4 цифр,
    // так как нам нужно учитывать "ведущие нули", например, в 0330.
    if (n < 0 || n > 9999) {
        std::cout << "Число должно быть от 0 до 9999." << std::endl;
        return 1;
    }

    // Извлекаем цифры числа:
    // a - первая цифра
    // b - вторая цифра
    // c - третья цифра
    // d - четвертая цифра
    int a = n / 1000;
    int b = (n / 100) % 10;
    int c = (n / 10) % 10;
    int d = n % 10;

    // Проверяем условие палиндрома
    if (a == d && b == c) {
        std::cout << n << " является палиндромом." << std::endl;
    } else {
        std::cout << n << " не является палиндромом." << std::endl;
    }

    return 0;
}
